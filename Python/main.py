# Imprimir algo por pantalla
print("hola mundo")

# Imprimir comillas en un print (escapadas \)
print("Hola mundo \"comillas\"")

# Para imprimir números no hace falta utilizar las comillas ""
print(100)

# Suma de números enteros
print(100 + 50)

# Encadenación de caracteres
print("hola " + "mundo")